package oct5;

public class Subtraction {

    public double subtraction(double a, double b){
        double sub = a - b;
        return sub;
    }
    public double subtraction(double a, double b, double c){
        double sub = a - b - c;
        return sub;
    }
    public double subtraction(double a, double b, double c, double d){
        double sub = a - b -c - d;
        return sub;
    }
}
