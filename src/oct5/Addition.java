package oct5;

public class Addition {

    public double sum(double a, double b){
        double sum = a + b;
        return sum;
    }

    public double sum(double a, double b, double c){
        double sum = a + b + c;
        return sum;
    }

    public double sum(double a, double b, double c, double d){
        double sum = a + b + c + d;
        return sum;
    }
}
