package sep21.operators;

public class Addition {
    public static void main(String[] args){

        //use arithmetic operators for practice
        int x = 40;
        int y = 30;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        x++;
        System.out.println(x);

        y--;
        System.out.println(y --);


    }
}
